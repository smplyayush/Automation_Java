package day1;

public class LogicalOperators {


    public static void main(String[] args) {

        int x = 10;
        int  y = 30;


        System.out.println(x<y);
        System.out.println(x>y);
        System.out.println(x<=y);
        System.out.println(x>=y);
        System.out.println(x==y);
        System.out.println(x!=y);

        System.out.println(x==10 && y>x);
        System.out.println(x==10 || y>x);

    }
}
