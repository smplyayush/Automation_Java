package day1;

public class Operators {

    public static void main(String[] args) {

        /**
         * +
         * -
         * *
         * /
         * %
         */

        int a = 10;
        int b = 20;

        int sum = a +b;
        int subs, mult, div;
        subs = b - a;
        mult = a*b;
        div = b/a;

        System.out.println("Addition: "+sum);
        System.out.println("Addition: "+subs);
        System.out.println("Addition: "+mult);
        System.out.println("Addition: "+div);


    }
}
